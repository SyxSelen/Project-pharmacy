    private void btnguardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnguardarMouseClicked
        // TODO add your handling code here:
        int codigo = Integer.parseInt(this.codigo.getText());
        String nombre = this.nmedicamento.getText();
        String cantidad = this.cantidad.getText();
        String precio = this.precio.getText();
        Date date = calendario.getDate();        
	DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String fecha = dateFormat.format(date);
        Laboratorio lab = (Laboratorio)laboratorio.getSelectedItem();
        Presentacion pre = (Presentacion)presentacion.getSelectedItem();
        Sitio sit = (Sitio)sitio.getSelectedItem();
        
        
        Medicamentos med = new Medicamentos();
        med.setCodigo(codigo);
        med.setNombre(nombre);
        med.setCantidad(cantidad);
        med.setPrecio(precio);
        med.setFechaCaducidad(date);
        med.setLaboratorioCodigo(lab);
        med.setPresentacionCodigo(pre);
        med.setSitioCodigo(sit);
        med.setDisponible(cantidad);
        
        try {
            medc.create(med);
            JOptionPane.showMessageDialog(null, "Registro guardado correctamente");
            limpiar();
            Cagartabla();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar los datos");
        }
    }//GEN-LAST:event_btnguardarMouseClicked
    
