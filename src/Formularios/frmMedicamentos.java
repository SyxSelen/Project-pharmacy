     public void Cagartabla()
 {
   
      DefaultTableModel modelo1=new  DefaultTableModel();
      String nombrecolumnas[]={"Codigo","Nombre","Presentacion","Laboratorio","Sitio","Cantidad","Disponible","Precio","Fecha venc"};
      modelo1.setColumnIdentifiers(nombrecolumnas);
     
      List lista=medc.findMedicamentosEntities();
      // llenando el modelo desde la base de datos
      DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
      for (int i = 0; i <lista.size(); i++) {
         Medicamentos p=(Medicamentos) lista.get(i);
         Object fila[]={p.getCodigo(),p.getNombre(),p.getPresentacionCodigo().getNombre(),p.getLaboratorioCodigo().getDescripcion(),p.getSitioCodigo().getNombre(),p.getCantidad(),p.getDisponible(),p.getPrecio(),dateFormat.format(p.getFechaCaducidad())};
         modelo1.addRow(fila);
     }
         
       tabla.setModel(modelo1);      
     
 
 }

